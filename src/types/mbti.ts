export const MBTI_MAP: Record<MBTI, MbtiInfo> =
  MBTI_LIST.reduce((acc, info) => ({ ...acc, [info.type]: info }), {} as Record<MBTI, MbtiInfo>)

export function randomSelfTalk(mbti: MBTI | undefined): string {
  if (!mbti) return '음...'
  const info = MBTI_MAP[mbti]
  return info.selfTalk[Math.floor(Math.random() * info.selfTalk.length)]
}
